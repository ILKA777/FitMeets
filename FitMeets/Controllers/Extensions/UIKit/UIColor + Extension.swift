import UIKit

extension UIColor {
    
    static func CustomYellowGreen() -> UIColor {
        return #colorLiteral(red: 0.5647058824, green: 0.9098039216, blue: 0, alpha: 1)
    }
    
    static func CustomBlue() -> UIColor {
        return #colorLiteral(red: 0.1490196078, green: 0.2352941176, blue: 1, alpha: 1)
    }
    
    static func CustomRed() -> UIColor {
        return #colorLiteral(red: 1, green: 0.2235294118, blue: 0.1176470588, alpha: 1)
    }
    
    static func CustomYellow() -> UIColor {
        return #colorLiteral(red: 1, green: 0.7215686275, blue: 0, alpha: 1)
    }
    
    static func CustomGray() -> UIColor {
        return #colorLiteral(red: 0.2745098039, green: 0.2745098039, blue: 0.2745098039, alpha: 1)
    }
    
}
